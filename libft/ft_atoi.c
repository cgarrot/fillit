#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int is_neg;
	int res;

	i = 0;
	is_neg = 0;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
		i++;
	str[i - 1] == '-' ? is_neg++ : is_neg;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (is_neg ? res * -1 : res);
}
